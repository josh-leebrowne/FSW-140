CREATE DATABASE avengers;

USE avengers;

CREATE TABLE avengers (url varchar(100), name varchar(50), appearances varchar(5), current varchar(3), gender varchar(6), probationaryIntro varchar(25), fullAvengersIntro varchar(25), year int, 
yearsSinceJoining int, honorary varchar(25), death1 varchar(5), return1 varchar(5), death2 varchar(5), return2 varchar(5), death3 varchar(5), return3 varchar(5), death4 varchar(5), return4 varchar(5),
 death5 varchar(5), return5 varchar(5), notes varchar(250));
 
 SELECT * FROM avengers;
